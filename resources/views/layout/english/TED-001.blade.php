          <h2 class="artTag1">1.本文 </h2>
          <h3 class="artTag2">1-1. Scriptを見よう</h3>

          <div class="artTag3-t">

            <div class="margin003 col-12 row"></div>

            <div class="quote">
              in 2009, cyclone nargis devastated myanmar. <br>
              2009年サイクロンナルギスミャンマーを襲いました 
            </div>
            <li><a href="https://ja.wikipedia.org/wiki/%E3%82%B5%E3%82%A4%E3%82%AF%E3%83%AD%E3%83%B3%E3%83%BB%E3%83%8A%E3%83%AB%E3%82%AE%E3%82%B9">サイクロン・ナルギス</a>はサイクロンの名称です。</li>
            <li>devastate : 他動詞 (土地を)荒らす</li>

            <div class="quote"> 
              millions of people were in severe need of help.<br>
              何百万人もの人々が深刻に援助を必要としていました 
            </div>
            <li>in need : 必要としている、不足している</li>

            <div class="quote">
              the u.n. wanted to rush people and supplies to the area.<br>
              国連は被災地に人員、物資を急派したかったのです.
            </div>
            <li>rush A to B : 急いでAをBに連れて行く、送る </li>

            <div class="quote">
              but there were no maps, no maps of roads, no maps showing hospitals, no way for help to reach the cyclone victims. <br>
              が、地図などありませんでした 道路も分からず、 病院の場所もわかりませんでした. 支援を被災者に届ける術がなかったのです
            </div>
            <li>victim : 犠牲者, 被害者</li>

            <div class="quote">
              When we look at a map of Los Angeles or London, it is hard to believe that as of 2005, only 15 percent of the world was mapped to a geo-codable level of detail. <br>
              ロサンゼルスやロンドンの地図を見てみると にわかには信じがたいのですが、 2005年当時、世界地図上には詳細に地理記号を用いて 地図化されていたのは世界のわずか15%でした 
            </div>
            <li>geo-codable : <a href="https://ja.wikipedia.org/wiki/%E3%82%B8%E3%82%AA%E3%82%B3%E3%83%BC%E3%83%87%E3%82%A3%E3%83%B3%E3%82%B0">ジオコーディング</a>にある通り、緯度・経度情報を与えられる、の意</li>

            <div class="quote">
              The U.N. ran headfirst into a problem that the majority of the world's populous faces: not having detailed maps.<br> 
              国連は、人口過密地域の大多数にあてはまった 詳細な地図がないという、この問題に 頭からぶち当たりました
            </div>

            <div class="quote">
              But help was coming. <br>しかし助けがやってきていました 
            </div>

            <div class="quote">
              At Google, 40 volunteers used a new software to map 120,000 kilometers of roads, 3,000 hospitals, logistics and relief points. <br>
              グーグルでは、40人のボランティアが 新たなソフトウェアを使って 120,000キロメートルにもなる道路、 3,000もの病院や避難地の地図を作製しました
            </div>
            <li>map : 他動詞　〜の地図を作る。 ここでは不定詞。直訳は〜の地図を作るためにソフトを使ったとなる。</li>
            <li>relief : 救助、救援、救援物資 / 安心、安堵、(不安などの)除去、軽減</li>
            <li>relief point : 避難地 (cf. refuge site, haven, evacation site)</li>

            <div class="quote">
              And it took them four days. The new software they used? Google Mapmaker. <br>
              地図の作成には4日かかりました その地図作成に使われたソフトウェアが何かって？それがグーグルマップメーカーです 
            </div>
            <li>it takes (時間) : 〜（時間）かかる。</li>

            <div class="quote">
              Google Mapmaker is a technology that empowers each of us to map what we know locally.<br>
              グーグルマップメーカーは私たち個人が知っている 現地の状況を地図化を助ける技術です
            </div>
            <li>empower 人 : 人に力を与える, 権限を与える </li>

            <div class="quote">
              People have used this software to map everything from roads to rivers, from schools to local businesses, and video stores to the corner store.<br>
              このソフトウェアは道路、 川、学校から地域の商店や、 ビデオ店、隅にある小さなお店まで、 全てを地図化するために利用されてきました 
            </div>
            <li>from A to B : AからBまで。　ここでは複数を並べ立てて'everything'の中身を具体的に例示していますね。</li>

            <div class="quote">
              Maps matter.<br>
              地図はとても重要です
            </div>
            <li>matter : 自動詞　重要である。 vitalやimportantも似たように使いますね。</li>

            <div class="quote">
              Nobel Prize nominee Hernando De Soto recognized that the key to economic liftoff for most developing countries is to tap the vast amounts of uncapitalized land.<br>
              ノーベル賞受賞者のエルナンド・デ・ソトも 多くの発展途上国において、まだ利用されていない広大な土地を 開発することが経済成長の鍵になると 認識していました
            </div>
            <li>nominee : 受賞者。ノミネートされた人。</li>
            <li><a href="https://ja.wikipedia.org/wiki/%E3%82%A8%E3%83%AB%E3%83%8A%E3%83%B3%E3%83%89%E3%83%BB%E3%83%87%E3%83%BB%E3%82%BD%E3%83%88">エルナンド・デ・ソト</a> : スペインの探検家。アメリカ、ミシシッピ川を白人として最初に発見した人。</li>

            <div class="quote">
              For example, a trillion dollars of real estate remains uncapitalized in India alone.<br>
              例えば、インド一国の中でも、 1兆ドルもの不動産が手つかずのままにされています 
            </div>
            <li>trillion : 1兆</li>
            <li>uncapitalize : 資本家する、現金化する、投資する</li>

            <div class="quote">
              In the last year alone, thousands of users in 170 countries have mapped millions of pieces of information, and created a map of a level of detail never thought viable.<br>
              前年だけで、170カ国で 何千もの人々が、測りきれないほど小さな情報のかけらを つなぎあわせ、以前は決して可視化できないと思われた 詳細な部分を網羅する地図を作り上げています
            </div>
            <li>alone : 名詞の後で、"ただ〜だけ、〜のみ"。ぜひ覚えたい表現</li>
            <li>viable : 実行可能な / 生存可能な。　文章では前者。feasibleに近い表現</li>

            <div class="quote">
              And this was made possible by the power of passionate users everywhere. <br>
              そしてこれは世界各地で奮起した利用者たちの 原動力のおかげで可能になったのです 
            </div>
            <li>passionate : 情熱的な、熱烈な / 多感な / 怒りやすい 文章では1個目の表現。</li>

            <div class="quote">
              Let's look at some of the maps being created by users right now.<br>
              それでは、たった今も利用者達によって 製作されている地図をいくつか見てましょう 
            </div>

            <div class="quote">
              So, as we speak, people are mapping the world in these 170 countries.<br>
              こうして話している間にも、170カ国で 地図が作製されているのです
            </div>

            <div class="quote">
              You can see Bridget in Africa who just mapped a road in Senegal.<br>
              これがセネガルで一本の道路を地図に登録してくれたアフリカのブリジット
            </div>

            <div class="quote">
              And, closer to home, Chalua, an N.G. road in Bangalore. <br>
              少し身近なところでは、バンガロールの N. G. ロードを登録してくれたチャルア 
            </div>

            <div class="quote">
              This is the result of computational geometry, gesture recognition, and machine learning.<br>
              これがコンピュータによる幾何学、ジェスチャー認識、 機械学習の結果です
            </div>
            <li>computational : コンピュータを使用した</li>
            <li>geometry : 幾何学</li>
            <li>gesture recognition : ジェスチャー認識学</li>
            <li>machine learning : 機械学習</li>

            <div class="quote">
              This is a victory of thousands of users, in hundreds of cities, one user, one edit at a time. <br>
              これは何百もの都市の 何千もの利用者たちと 一人ひとりの小さな編集による勝利です 
            </div>

            <div class="quote">
              This is an invitation to the 70 percent of our unmapped planet. Welcome to the new world. <br>
              これは私たち地球のいまだ地図化されていない 70%への招待状なのです 新たな世界へようこそ
            </div>
          </div>

