          <h3 class="artTag2">1-1. Scriptを見よう</h3>

          <div class="artTag3-t">
            <div class="margin003 col-12 row"></div>

            <div class="quote">
            As a boy, I loved cars.<br>
            男の子がみんなそうであるように 私も車が好きでした 
            </div>

            <div class="quote"> 
            When I turned 18, I lost my best friend to a car accident.<br>
            18のときに 一番の親友を自動車事故でなくしました 
            </div>
            <li>turn (年齢) : (年齢)になる</li>
            <li>to : due to と同じように使っている</li>

            <div class="quote">
            Like this. <br>
            あっけなく 
            </div>
            <li>指パッチンが時間を示しており、これだけの一瞬のうちに、という意味です</li>

            <div class="quote">
            And then I decided I'd dedicate my life to saving one million people every year.<br>
            それ以来 私の使命になったのは 年に百万という人を 事故から救えないかということです 
            </div>
            <li>dedicate A(時間) to B : AをBに捧げる。専念する。</li>

            <div class="quote">
            Now I haven't succeeded, so this is just a progress report, but I'm here to tell you a little bit about self-driving cars.<br>
            まだ未完成なので 中間報告になりますが 自動運転車について 少しお話しします 
            </div>
            <li>Now S V, S' V' : 今や SがVである以上（であるので）, S' V' </li>

            <div class="quote">
            I saw the concept first in the DARPA Grand Challenges where the U.S. government issued a prize to build a self-driving car that could navigate a desert. <br>
            この概念に初めて触れたのは DARPAグランドチャレンジでした 砂漠を切り抜けた自動運転車に アメリカ政府が 賞金を出すことにしたのです 
            </div>

            <div class="quote">
            And even though a hundred teams were there, these cars went nowhere. <br>
            100以上のチームが参加したにもかかわらず ゴールにたどり着けたチームはありませんでした 
            </div>

            <div class="quote">
            So we decided at Stanford to build a different self-driving car. <br>
            スタンフォード大では 新たに自動運転車を作ることにしました 
            </div>
            <li>self-driving : 自動運転の</li>

            <div class="quote">
            We built the hardware and the software. <br>
            ハードとソフトの両方を開発しました 
            </div>

            <div class="quote">
            We made it learn from us, and we set it free in the desert. <br>
            人間から学習させ 砂漠に放ちました 
            </div>
            <li>set free A : Aを解き放つ、自由にする</li>

            <div class="quote">
            And the unimaginable happened: it became the first car to ever return from a DARPA <br>
            そして信じがたいことを実現しました   DARPAグランドチャレンジで 戻ってきた初めての車となったのです 
            </div>
            <li>unimaginalble : 信じられない、思いもよらない</li>

            <div class="quote">
            Grand Challenge, winning Stanford 2 million dollars. <br>
            スタンフォード大は 200万ドルの賞金を手にしました 
            </div>

            <div class="quote">
            Yet I still hadn't saved a single life. <br>
            でもまだ誰の命も救ってはいません 
            </div>

            <div class="quote">
            Since, our work has focused on building driving cars that can drive anywhere by themselves <br>
            それ以来 私たちは 自分でどこへでも行ける 自動運転車に取り組んでいます 
            </div>
            <li>focuse on ~ing : 〜に集中して取り組む</li>

            <div class="quote">
            -- any street in California.  We've driven 140,000 miles. <br>
            カリフォルニアのあらゆる通りを 22万キロ走ってきました 
            </div>

            <div class="quote">
            Our cars have sensors by which they magically can see everything around them and make decisions about every aspect of driving.<br>
            センサーを搭載していて 魔法のように 周囲の状況を把握し 運転にかかわる あらゆる判断を行います 
            </div>
            <li>magically : 魔法のように</li>
            <li>aspect : 角度・見地 / 面・様相 / 方位・向き</li>

            <div class="quote">
            It's the perfect driving mechanism.<br>
            完全な運転機構を備えています 
            </div>

            <div class="quote">
            We've driven in cities, like in San Francisco here. <br>
            ご覧のように サンフランシスコの 街中を走っています 
            </div>

            <div class="quote">
            We've driven from San Francisco to Los Angeles on Highway 1. <br>
            州道1号線を サンフランシスコからロサンゼルスへと走りました 
            </div>

            <div class="quote">
            We've encountered joggers, busy highways, toll booths, and this is without a person in the loop; the car just drives itself. <br>
            ジョギングする人にも出会い 混雑した高速道路や 料金所を通り 人手を介さずに 車が自分で運転しています
            </div>
            <li>jogger : ジョギングする人</li>
            <li>tool booth : 料金所　toolだけでも。</li>
            <li>in the loop : コミュニケーションに入っている</li>

            <div class="quote">
            In fact, while we drove 140,000 miles, people didn't even notice.<br>
            22万キロも走ったというのに 誰も気づかないのです 
            </div>
            <li>dive - drove - driven</li>

            <div class="quote">
            Mountain roads, day and night, and even crooked Lombard Street in San Francisco. 
            山道を越え 夜となく 昼となく 曲がりくねったサンフランシスコの ロンバード通りも 意に介さず 
            </div>
            <li>crook : 他動詞 湾曲させる, 曲げる / 名　鍵、湾曲、曲がったもの</li>

            <div class="quote">
            (Laughter) Sometimes our cars get so crazy, they even do little stunts. <br>
            (笑) 時にはちょっとした スタントまでやってのけます 
            </div>
            <li>stunt : スタント</li>

            <div class="quote">
            (Video) Man: Oh, my God. What? Second Man: It's driving itself. <br>
            うわーっ マジすか？ 車がやってるんだよ 
            </div>

            <div class="quote">
            Sebastian Thrun: Now I can't get my friend Harold back to life, but I can do something for all the people who died. <br>
            友達のハロルドの命を取り戻すことはできませんが 亡くなったみんなのためにできることがあります 
            </div>

            <div class="quote">
            Do you know that driving accidents are the number one cause of death for young people? <br>
            車の事故が 若者の死因の第1位なのはご存じですか？ 
            </div>

            <div class="quote">
            And do you realize that almost all of those are due to human error and not machine error, and can therefore be prevented by machines? <br>
            そのほとんどは 車の問題ではなく 人間のミスによるということを？ これは機械の力によって防ぎうることなのです 
            </div>
            <li>prevent : 予防する / 妨げる</li>

            <div class="quote">
            Do you realize that we could change the capacity of highways by a factor of two or three if we didn't rely on human precision on staying in the lane -- improve body position and therefore drive a little bit closer together on a little bit narrower lanes, and do away with all traffic jams on highways?<br>
            人間の精度で 車道を走るのをやめれば 高速道路を走る車の量を 今の2倍から3倍に 増やすことができます 車の位置を調整して もう少し車間を狭くし レーンの幅も狭めるなら 高速道路の渋滞はなくせます 
            </div>
            <li>do away with 〜 : 〜を廃止する、なくす</li>

            <div class="quote">
            Do you realize that you, TED users, spend an average of 52 minutes per day in traffic, wasting your time on your daily commute? <br>
            みなさんは 毎日の通勤のため 平均して 52分もの時間を 道路の上で 無駄にしています
            </div>
            <li>waste A on B : BのためにAを無駄にする, BにAを費やす</li>

            <div class="quote">
            You could regain this time. <br>
            これは取り戻せる時間です
            </div>
            <li>regain : 取り戻す</li>

            <div class="quote">
            This is four billion hours wasted in this country alone. <br>
            アメリカだけで 40億時間の無駄です
            </div>

            <div class="quote">
            And it's 2.4 billion gallons of gasoline wasted. <br>
            91億リットルのガソリンが無駄になっています 
            </div>

            <div class="quote">
            Now I think there's a vision here, a new technology, and I'm really looking forward to a time when generations after us look back at us and say how ridiculous it was that humans were driving cars. <br>
            ここにビジョンがあり 新しいテクノロジーがあります 後の世代の人々が振り返って 車を人間が運転していたなんて まったく馬鹿げていると思うようになることを期待しています
            </div>

